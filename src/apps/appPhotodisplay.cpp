void AppPhotodisplay::setup()
{
    display.clearScreen();
    if (GUI::msgbox_yn("是否显示静态照片", "请选择", "退出", "继续"))
    {
        appManager.goBack();
    }
    else
    {
        const char *path = GUI::fileDialog("请选择要显示的lbm图片");
        if (path == NULL)
        {
            appManager.goBack();
            return;
        }
        GUI::drawLBM(0, 0, path, 0);
        display.display(false);
        Serial.println("刷新完成...正在关机");
        hal.powerOff(0);
    }
}
